
package seminarioP;


public class local {
    
    private int id_local;
    private String nome_local;
    private int nota_acessibilidade;
    private  String comentario_local;

    public int getId_local() {
        return id_local;
    }

    public void setId_local(int id_local) {
        this.id_local = id_local;
    }

    public String getNome_local() {
        return nome_local;
    }

    public void setNome_local(String nome_local) {
        this.nome_local = nome_local;
    }

    public int getNota_acessibilidade() {
        return nota_acessibilidade;
    }

    public void setNota_acessibilidade(int nota_acessibilidade) {
        this.nota_acessibilidade = nota_acessibilidade;
    }

    public String getComentario_local() {
        return comentario_local;
    }

    public void setComentario_local(String comentario_local) {
        this.comentario_local = comentario_local;
    }
    
    
}
